# Сократить точность чисел с плавающей точкой
def reduce_precision(path, num_digits = 0):
    _typ = int if num_digits == 0 else float
    return re.sub(r'\d+\.\d+', lambda s: str(_typ(round(float(s.group()), num_digits))), path)

# Трансформировать svg
def transform_path(path, scale_x, scale_y, rot_deg, dx, dy):
    def process_point(point_str):
        x, y = float(point_str.split(',')[0]), float(point_str.split(',')[1])
        x *= scale_x
        y *= scale_y
        x1 = x * math.cos(rot_deg * math.pi / 180) - y * math.sin(rot_deg * math.pi / 180)
        y1 = y * math.cos(rot_deg * math.pi / 180) + x * math.sin(rot_deg * math.pi / 180)
        x1 += dx
        y1 += dy
        return f'{x1},{y1}'
    path = re.sub(r'[\d\.]+,[\d\.]+', lambda x: process_point(x.group()), path)
    return path

# Процессинг SVG
def process_path(path, scale_x, scale_y, rot_deg, dx, dy, decimals):
    # убираем начальную точку
    path = re.sub(r'M [\d\.,]+ L', 'M', path, count=1)
    # убираем конечную точку
    path = re.sub(r' L [\d\.,]+ L [\d\.,]+ Z', '', path, count=1)
    # трансформация
    path = transform_path(path, scale_x, scale_y, rot_deg, dx, dy)
    # оптимизация размера файла
    path = reduce_precision(path, decimals)
    return path

