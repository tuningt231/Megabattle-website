# Сократить точность чисел с плавающей точкой
def reduce_precision(path, num_digits = 0):
    _typ = int if num_digits == 0 else float
    return re.sub(r'\d+\.\d+', lambda s: str(_typ(round(float(s.group()), num_digits))), path)

# Процессинг SVG
def process_path(path, delta_y=0):
    # убираем начальную точку
    path = re.sub(r'M [\d\.,]+ L', 'M', path, count=1)
    # убираем конечную точку
    path = re.sub(r' L [\d\.,]+ L [\d\.,]+ Z', '', path, count=1)
    # применяем delta_y
    path = re.sub(r',(\d+\.*\d*)', lambda s: f',{float(s.group(1)) + delta_y}', path)
    return reduce_precision(path)

